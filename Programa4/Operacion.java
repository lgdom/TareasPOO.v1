public class Operacion {
    
    public int suma (int ent1, int ent2){
        return ent1 + ent2;
    }
    
    public double suma (int ent3, double dob1){
        return (double) ent3 + dob1;
    }
    
    public double suma (double dob2, double dob3, double dob4){
        return dob2 + dob3 + dob4;
    }
 
    public int resta (int ent1, int ent2){
        return ent1 - ent2;
    }
    
    public double resta (int ent3, double dob1){
        return (double) ent3 - dob1;
    }
    
    public double resta (double dob2, double dob3, double dob4){
        return dob2 - dob3 - dob4;
    }
    
    public int multiplicacion (int ent1, int ent2){
        return ent1 * ent2;
    }
    
    public double multiplicacion (int ent3, double dob1){
        return (double) ent3 * dob1;
    }
    
    public double multiplicacion (double dob2, double dob3, double dob4){
        return dob2 * dob3 * dob4;
    }
    
    public int division (int ent1, int ent2){
        return ent1 / ent2;
    }
    
    public double division (int ent3, double dob1){
        return (double) ent3 / dob1;
    }
    
    public double division (double dob2, double dob3, double dob4){
        return dob2 / dob3 / dob4;
    }
        
}

    
    